package com.quliantrip.qulian.adapter.myAdapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.ArrayList;

/**
 * Created by Qulian5 on 2016/1/12.
 */
public class RegisterStyleAdapter extends FragmentPagerAdapter {
    private ArrayList<Fragment> fragments;

    public RegisterStyleAdapter(FragmentManager fm, ArrayList<Fragment> fragments) {
        super(fm);
        this.fragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return fragments.get(position);
    }

    @Override
    public int getCount() {
        return fragments.size();
    }

}
